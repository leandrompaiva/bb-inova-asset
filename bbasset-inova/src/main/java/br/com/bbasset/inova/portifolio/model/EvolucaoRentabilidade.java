package br.com.bbasset.inova.portifolio.model;

public class EvolucaoRentabilidade {

	private String mesAnoRentabilidade;
	private double valorRentabilidade;

	public String getMesAnoRentabilidade() {
		return mesAnoRentabilidade;
	}

	public void setMesAnoRentabilidade(String mesAnoRentabilidade) {
		this.mesAnoRentabilidade = mesAnoRentabilidade;
	}

	public double getValorRentabilidade() {
		return valorRentabilidade;
	}

	public void setValorRentabilidade(double valorRentabilidade) {
		this.valorRentabilidade = valorRentabilidade;
	}

}
